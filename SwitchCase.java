public class SwitchCase {
    public static void main(String[] args){
        int mes = 2;

        switch (mes) {

            case 1:
                System.out.println("mês 1");
                break;

            case 2:
                System.out.println("mês 2");
                break;
            
            default:
                System.out.println("Não é mês 1 ou 2");

        }
    }    
}
